/** Idea-generation prompt (Layer 1) — asks for a batch of fresh topic ideas
 * spread across the magazine's five sub-verticals. The generator adds the
 * structured-output schema on top. */
export function defaultIdeaPrompt(m: Magazine): string {
  return `तुम पत्रिका ऐप के लिए एक सीनियर हिंदी कंटेंट स्ट्रैटेजिस्ट हो। नीचे दी गई मैगज़ीन के लिए 12–15 नए, ओरिजिनल टॉपिक-आइडिया बनाओ — पांचों सब-वर्टिकल में फैलाकर (हर सब-वर्टिकल से कम से कम 2)।

मैगज़ीन: ${m.nameHi}
पॉजिशनिंग: ${m.tagline}
टारगेट पाठक: ${m.reader} (उम्र ${m.age})
क्षेत्र फोकस: राजस्थान और हिंदी पाठक
सब-वर्टिकल: ${m.subVerticals.join(" | ")}

नियम:
• हर आइडिया में हो: (1) हिंदी हेडलाइन, (2) सब-वर्टिकल का नाम, (3) एक लाइन में हुक/एंगल, (4) पाठक को क्या फायदा।
• एंगल 'हटकर' और प्रॉब्लम-सॉल्विंग हो — घिसे-पिटे नहीं। जहां हो सके राजस्थान/लोकल कनेक्ट डालो।
• कंप्लायंस: ${m.compliance}`;
}

/** Content-generation prompt (Layer 3) — the master prompt for a full premium
 * article on a chosen topic. The generator appends the actual topic. */
export function defaultContentPrompt(m: Magazine): string {
  return `तुम राजस्थान पत्रिका के "${m.nameHi}" डेस्क के अनुभवी हिंदी पत्रकार हो। नीचे दिए टॉपिक पर एक प्रीमियम आर्टिकल लिखो, जो पत्रिका ऐप पर पब्लिश होगा।

पाठक: ${m.reader}
सब-वर्टिकल में से चुनें: ${m.subVerticals.join(" / ")}

फॉर्मेट व नियम — पत्रिका एडिटोरियल चेकलिस्ट:
• लंबाई 600–1000 शब्द। आम आदमी की भाषा, छोटे-छोटे पैराग्राफ, पढ़ने में आसान।
• स्ट्रक्चर: आकर्षक हेडलाइन → हुक इंट्रो → समस्या/संदर्भ → ${m.structure} → निष्कर्ष व पाठक के लिए अगला कदम (CTA)।
• नया एंगल और प्रॉब्लम-सॉल्विंग अनिवार्य। कम से कम 1 एक्सपर्ट-कोट या डेटा-रेफरेंस डालो; जहां असली सोर्स चाहिए वहां "[सोर्स/लिंक जोड़ें]" मार्कर छोड़ो — फर्जी आंकड़े या नाम मत गढ़ो।
• SEO: हेडलाइन में मुख्य कीवर्ड रखो और अंत में 3 सुझाए गए टैग दो।
• साथ में दो: (अ) WhatsApp शेयर के लिए 2-लाइन टीज़र, (ब) एक इन्फोग्राफिक के लिए 4–5 बुलेट पॉइंट।
• कंप्लायंस: ${m.compliance}
आउटपुट: सिर्फ हिंदी (देवनागरी) में।`;
}
